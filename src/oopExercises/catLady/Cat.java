package oopExercises.catLady;

public class Cat {
    private String breed;
    private String name;

    public Cat() {
    }

    public Cat(String breed, String name) {
        this.breed = breed;
        this.name = name;
    }

    public String getBreed() {
        return breed;
    }

    public String getName() {
        return name;
    }
}
